//0 ao 50
let cont = 50;
while (cont >= 0) {
  console.log(`Número = ${cont}`);
  cont--;
}
console.log();

//Contagem regressiva 10 ao 1
let i = 10;
do {
  console.log(`contagem regressiva: ${i}`);
  i--;
} while (i > 0);
console.log();

//Números pares de 0 a 100
for (let pares = 0; pares <= 100; pares += 2) {
  console.log(`Pares: ${pares}`);
}

